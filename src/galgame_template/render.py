def _render_bootstrap(manifest: ProjectManifest) -> str:
    return f"""# Runtime helpers for the generated phase-1 scaffold

default custom_auto_forward_seconds = {manifest.ui.default_auto_forward_seconds}
default custom_seconds_per_char = {manifest.ui.default_seconds_per_char}
default custom_text_cps = {manifest.ui.default_text_cps}
default interface_hidden = False
default auto_mode_enabled = False

init python:
    def toggle_interface():
        renpy.store.interface_hidden = not renpy.store.interface_hidden

    def set_auto_forward_seconds(value):
        renpy.store.custom_auto_forward_seconds = float(value)
        renpy.game.preferences.afm_time = float(value)

    def set_text_seconds_per_char(value):
        numeric = max(0.01, float(value))
        renpy.store.custom_seconds_per_char = numeric
        renpy.store.custom_text_cps = max(1, int(round(1.0 / numeric)))
        renpy.game.preferences.text_cps = renpy.store.custom_text_cps

    def start_auto_mode():
        renpy.store.auto_mode_enabled = True

    def stop_auto_mode():
        renpy.store.auto_mode_enabled = False

    def save_or_confirm(slot_name):
        if renpy.slot_json(slot_name):
            renpy.show_screen("confirm_save_overwrite", slot_name)
        else:
            renpy.save(slot_name)

    def overwrite_save(slot_name):
        renpy.save(slot_name)
"""


