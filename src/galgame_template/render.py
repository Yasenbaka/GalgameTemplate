@dataclass(frozen=True, slots=True)
class RenderedFile:
    relative_path: Path
    content: str


def build_render_plan(manifest: ProjectManifest) -> tuple[RenderedFile, ...]:
    return (
        RenderedFile(Path(GENERATED_README_NAME), _render_generated_readme(manifest)),
        RenderedFile(Path("game") / "options.rpy", _render_options(manifest)),
        RenderedFile(Path("game") / "gui.rpy", _render_gui(manifest)),
        RenderedFile(Path("game") / "00_bootstrap.rpy", _render_bootstrap(manifest)),
        RenderedFile(Path("game") / "10_characters.rpy", _render_characters()),
        RenderedFile(Path("game") / "20_audio.rpy", _render_audio()),
        RenderedFile(Path("game") / "30_transforms.rpy", _render_transforms()),
        RenderedFile(Path("game") / "40_screens_main_menu.rpy", _render_main_menu()),
        RenderedFile(Path("game") / "41_screens_say.rpy", _render_say(manifest)),
        RenderedFile(Path("game") / "42_screens_hud.rpy", _render_hud()),
        RenderedFile(Path("game") / "43_screens_save_load.rpy", _render_save_load(manifest)),
        RenderedFile(Path("game") / "44_screens_preferences.rpy", _render_preferences()),
        RenderedFile(Path("game") / "45_screens_history.rpy", _render_history(manifest)),
        RenderedFile(Path("game") / "46_screens_confirm.rpy", _render_confirm()),
        RenderedFile(Path("game") / "50_script_prologue.rpy", _render_prologue()),
        *(RenderedFile(path / ".gitkeep", "") for path in ASSET_DIRECTORIES),
    )


def write_render_plan(
    output_dir: Path,
    render_plan: Iterable[RenderedFile],
    *,
    force: bool = False,
) -> tuple[Path, ...]:
    written_files: list[Path] = []

    for rendered_file in render_plan:
        destination = output_dir / rendered_file.relative_path
        destination.parent.mkdir(parents=True, exist_ok=True)

        if destination.exists() and destination.is_dir():
            raise IsADirectoryError(f"Refusing to overwrite directory with file: {destination}")

        if destination.exists() and not force:
            raise FileExistsError(f"Refusing to overwrite existing file: {destination}")

        with destination.open("w", encoding="utf-8", newline="\n") as handle:
            handle.write(rendered_file.content)

        written_files.append(destination)

    return tuple(written_files)


def _render_generated_readme(manifest: ProjectManifest) -> str:
    source_name = manifest.source_path.name if manifest.source_path else "manifest.toml"
    return f"""# {manifest.project.window_title}

This directory was generated by the phase-1 `galgame_template` scaffold from `{source_name}`.

The generated `.rpy` files are placeholder contract files, not final visual production assets.
They exist so the repo can express the non-visual runtime foundation described in `docs/项目程序需求.txt`
without requiring a local Ren'Py SDK.

## Captured baseline

- save pages: {manifest.ui.save_pages}
- slots per page: {manifest.ui.slots_per_page}
- total save slots: {manifest.ui.save_slot_count}
- auto forward seconds: {manifest.ui.default_auto_forward_seconds}
- seconds per character: {manifest.ui.default_seconds_per_char}
- history length: {manifest.ui.history_length}

Future polished template assets can replace these files later via `{FUTURE_TEMPLATE_ROOT.as_posix()}`.
"""


